#import "RMCollageCell.h"

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////

@interface RMCollageCell()
@property (nonatomic) UIImageView *collageImageView;
@end

@implementation RMCollageCell

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#pragma mark - Init

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
- (instancetype)initWithStyle:(UITableViewCellStyle)style reuseIdentifier:(NSString *)reuseIdentifier
{
  self = [super initWithStyle:style reuseIdentifier:reuseIdentifier];
  if (self)
  {
    // Image view
    _collageImageView = [[UIImageView alloc] initWithFrame:CGRectMake(7.0f, 7.0f, 306.0f, 306.0f)];
    _collageImageView.contentMode = UIViewContentModeScaleAspectFit;
    [self.contentView addSubview:_collageImageView];
  }
  return self;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#pragma mark - Setters

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
- (void)setCollage:(RMCollage *)collage
{
  _collage = collage;
  _collageImageView.image = collage.previewImage;  
}

@end

#import "RMCollageScene.h"

static CGSize kCollageSize = { 288.0f , 288.0f };

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////

@implementation RMCollageScene

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#pragma mark - Init

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
- (id)initWithCollage:(RMCollage *)collage size:(CGSize)size
{
  self = [super initWithSize:size];
  if (self)
  {
    _collage = collage;
    self.backgroundColor = [UIColor whiteColor];
    
    SKShapeNode *baseNode = [SKShapeNode shapeNodeWithRect:CGRectMake(0.0f, 0.0f, kCollageSize.width, kCollageSize.height)];
    baseNode.position = CGPointMake((size.width - kCollageSize.width) / 2, (size.height - kCollageSize.height) / 2);
    baseNode.strokeColor = [UIColor blackColor];
    baseNode.lineWidth = 1.0f;
    [self addChild:baseNode];
  }
  return self;
}

@end
